#include <iostream>
#include <vector>
#include <unordered_map>
#include <algorithm>

using namespace std;

void depth_first_search(unordered_map<int, vector<int>> &connections, vector<int> &visited, int start_node, int depth)
{
    if (depth == 0)
    {
        return;
    }
    cout << start_node << " ";
    for (auto x : connections[start_node])
    {
        if (!visited[x])
        {
            visited[x] = 1;
            depth_first_search(connections, visited, x, depth - 1);
        }
    }
}

